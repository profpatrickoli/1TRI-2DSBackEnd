// BOOLEANOS: "true" e "false"
/* Operadores de comparação:
== -> igualdade
!= -> desigualdade
>  -> maior que
<  -> menor que
>= -> maior ou igual que
<= -> menor ou igual que
* Operador de igualdade estrita *
=== -> igualdade estrita (compara conteúdo e tipo da variável)
!== -> desigualdade estrita
*/

let estudante = true
let estudiosos = false

// comparador de igualdade: compara apenas o conteúdo
console.log(true == 1)
console.log(false == 0)
console.log("1" == 1)

// comparador de igualdade estrita: compara conteúdo e tipo
console.log(true === 1)
console.log(false === 0)
console.log("1" === 1)

